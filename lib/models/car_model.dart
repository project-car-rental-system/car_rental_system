// lib/models/car_model.dart
class CarModel {
  final String name;
  final String image;
  final String price;
  final String speed;
  final String battery;
  final String volume;
  final String seats;
  final String soundSystem;

  CarModel({
    required this.name,
    required this.image,
    required this.price,
    required this.speed,
    required this.battery,
    required this.volume,
    required this.seats,
    required this.soundSystem,
  });
}
